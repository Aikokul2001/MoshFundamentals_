public class ComparisonOperators {
    public static void main(String[] args) {
        int x = -598;
        int y = 854;
        System.out.println(x == y);  // boolean expression == equal
        System.out.println(x != y); // != not equal, how the expressions shows
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y );
        System.out.println(x >= y);

    }
}

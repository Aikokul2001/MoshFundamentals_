public class OrderOfOperations {
    public static void main(String[] args) {
        int x = 10+3*2;
        System.out.println(x);
        int y = (10+3)*2;
        System.out.println(y);
        int z = 150/8+9*12-10;
        System.out.println(z);
        int u = 8100/90+10/2-50;
        System.out.println(u);
    }

}
